namespace Qwerteesharp.Models {
    /// <summary>
    /// Reprensents a print.
    /// </summary>
    public class Print {
        /// <summary>
        /// Print's id.
        /// </summary>
        public string Id { get; set; }

        /// <summary>
        /// Print's name.
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Print's author.
        /// </summary>
        public string Author { get; set; }

        /// <summary>
        /// Print's image's url.
        /// </summary>
        public string ImageUrl { get; set; }

        /// <summary>
        /// Print's link.
        /// </summary>
        public string Link { get; set; }
    }
}
